defmodule QuickBEAM.JS.Parser.Diagnostics.ArrowDestructuredDuplicateParameterTest do
  use ExUnit.Case, async: true
  @moduletag :quickjs_port

  alias QuickBEAM.JS.Parser
  alias QuickBEAM.JS.Parser.AST

  test "ports QuickJS arrow destructured duplicate parameter diagnostics" do
    source = "value = ({ a }, [a]) => a;"

    assert {:error, %AST.Program{}, errors} = Parser.parse(source)

    assert Enum.any?(
             errors,
             &(&1.message == "duplicate parameter name not allowed in strict mode")
           )
  end
end
